#ifndef LLVM_LIB_TARGET_YCORE_YCORESUBTARGET_H
#define LLVM_LIB_TARGET_YCORE_YCORESUBTARGET_H

#include "YCoreFrameLowering.h"
#include "YCoreISelLowering.h"
#include "YCoreInstrInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "YCoreGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class YCoreSubtarget : public YCoreGenSubtargetInfo {
  virtual void anchor();
  YCoreInstrInfo InstrInfo;
  YCoreFrameLowering FrameLowering;
  YCoreTargetLowering TLInfo;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  ///
  YCoreSubtarget(const Triple &TT, const std::string &CPU,
                 const std::string &FS, const TargetMachine &TM);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const YCoreInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const YCoreFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const YCoreTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const TargetRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
};
} // End llvm namespace

#endif
